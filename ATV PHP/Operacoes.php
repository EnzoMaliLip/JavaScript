<?php

$valor1 = 2;
$valor2 = 4;

function somar($valor1, $valor2) {
    return $valor2 + $valor1;
}

function subtrair($valor1, $valor2) {
    return $valor2 - $valor1;
}

function multiplicar($valor1, $valor2) {
    return $valor2 * $valor1;
}

function dividir($valor1, $valor2) {
    return $valor2 / $valor1;
}