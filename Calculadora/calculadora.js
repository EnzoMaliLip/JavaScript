function somaNumeros(num1, num2) {
    return num1 + num2;
}
console.log(somaNumeros(7, 9))

function subtraiNumeros(num1, num2) {
    return num1 - num2;
}
console.log(subtraiNumeros(7, 9))

function multiplicacao(num1, num2) {
    return num1 * num2;
}
console.log(multiplicacao(7, 9))

function divisao(num1, num2) {
    return num1 / num2;
}
console.log(divisao(7, 9))

function restoDivisao(num1, num2) {
    return num1 % num2
}
console.log(restoDivisao(10, 9))